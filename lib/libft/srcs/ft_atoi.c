int		ft_atoi(const char *str)
{
	int					sign;
	unsigned long int	n;

	n = 0;
	sign = 1;
	while (*str == ' ' || *str == '\t' || *str == '\r' || *str == '\f' ||
			*str == '\v' || *str == '\n')
		str++;
	if (*str == '-')
		sign = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		if ((n > 922337203685477580 || (n == 922337203685477580
					&& (*str - '0') > 7)) && sign == 1)
			return (-1);
		else if ((n > 922337203685477580 || (n == 922337203685477580
						&& (*str - '0') > 8)) && sign == -1)
			return (0);
		n = 10 * n + (unsigned long int)(*str - '0');
		str++;
	}
	return ((int)(sign * (int)n));
}
