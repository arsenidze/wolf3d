#include "libft.h"

void	ft_swap_str(char **s1, char **s2)
{
	char	*tmp;

	if (!s1 && !s2 && !*s1 && !*s2)
	{
		tmp = *s1;
		*s1 = *s2;
		*s2 = tmp;
	}
}
