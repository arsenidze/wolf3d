#include "libft.h"

void	ft_arr_foreach(int *arr, size_t n, void (*f)(int *))
{
	size_t	i;

	if (!arr || !f)
		return ;
	i = 0;
	while (i < n)
	{
		(*f)(&arr[i]);
		i++;
	}
}
