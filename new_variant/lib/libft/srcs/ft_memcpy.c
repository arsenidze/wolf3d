#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	if (dst == NULL && src == NULL)
		return (NULL);
	i = 0;
	while (i < n)
	{
		*(t_byte *)(dst + i) = *(t_byte *)(src + i);
		i++;
	}
	return ((t_byte *)dst);
}
