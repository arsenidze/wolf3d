static int	get_size(char **lines)
{
	int		i;

	i = 0;
	while (lines[i])
	{
		i++;
	}
	return (i);
}

void		*free_map(int ***map)
{
	int		j;

	j = 0;
	while ((*map)[j])
	{
		free((*map)[j]);
		j++;
	}
	free(*map);
	*map = NULL;
	return (NULL);
}

int			**create_map(char **lines, int *w, int *h)
{
	int		**map;
	int		j;
	int		i;
	int		nlines;
	int		line_len;

	nlines = get_size(lines);
	line_len = ft_strlen(lines[0]);
	if (!(map = malloc(sizeof(int *) * (nlines + 1))))
		return (NULL);
	map[nlines] = NULL;
	j = 0;
	while (j < nlines)
	{
		if (!(map[j] = malloc(sizeof(int) * line_len)))
			return (free_map(&map));
		i = 0;
		while (i < line_len)
		{
			map[j][i] = lines[j][i] - '0';
			i++;
		}
		j++;
	}
	*w = line_len;
	*h = nlines;
	return (map);
}
