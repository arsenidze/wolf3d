#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (*(t_byte *)(s + i) == (t_byte)c)
			return ((t_byte *)(s + i));
		i++;
	}
	return (NULL);
}
